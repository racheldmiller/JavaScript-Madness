// --------------------------- QUESTION ---------------------------------
// What are some types of values we could have when using variables? And what do they mean?

// BOOLEAN: true or false
// NULL: non-existent
// NUMBERS: integers
// STRING: a bunch of characters
// UNDEFINED: no value yet

// ------------------------------ EXAMPLE ----------------------------------
// For demonstrative purposes, I'm going to give an example of a STRING, NUMBER, and BOOLEAN.

// STRING
// Note: Notice how if it's a string, it needs to be in quotes?
var name = "Ryan";

// NUMBER
// Note: Notice how I can write out the integer without quotes?
var age = 25;

// BOOLEAN
// Note: in this case, it's true. Alternatively, I could set this to false.
var isDeveloper = true;

// ---------------------------- TESTING -----------------------------------
var name = "Ryan";
console.log(name);

var age = 25;
console.log(age);

var isDeveloper = true;
console.log(isDeveloper);
